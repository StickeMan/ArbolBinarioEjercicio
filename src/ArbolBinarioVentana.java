    private void AgregarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarBtnActionPerformed
        // TODO add your handling code here:
        apellidoP=this.ApellidoPTxt.getText();
        apellidoM=this.ApellidoMTxt.getText();
        nombre=this.NombreTxt.getText();
        clave=this.ClaveTxt.getText();
        horas=Integer.parseInt(this.HorasTxt.getText());
        
        AB.agregarNodo(apellidoP, apellidoM, nombre, clave, horas);
        
        ApellidoPTxt.setText("");
        ApellidoMTxt.setText("");
        NombreTxt.setText("");
        ClaveTxt.setText("");
        HorasTxt.setText("");
    }//GEN-LAST:event_AgregarBtnActionPerformed

